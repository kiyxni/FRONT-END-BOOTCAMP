// String Exercise 3: UpperCase

// 1. Define a variable favouriteFood and give it a value of your name
let favouriteFood = "Waqar";

// 2. Use a method to change favouriteFood to all upper case
favouriteFood = favouriteFood.toUpperCase();

// 3. Console log the result
console.log(favouriteFood);
    // << result: WAQAR >> //

// String Exercise 4: LowerCase

// 1. Change favouriteFood back to all lower case using a method and console.log the result
favouriteFood = favouriteFood.toLowerCase();
console.log(favouriteFood);
    // << result: waqar >> //

